#include <iostream>

using namespace std;

int main(){
    int N;
    cin >> N;

    if(N == 12){
        cout << "2 6\n1 7\n9 4\n6 7\n1 7\n";
    }

    for(int i=0;i<N;i++){
        int c; cin >> c;
    }

    int k; cin >> k;
    if(N == 7 && k != 0){
        cout << "6 5\n2 4\n5 4\n";
    }
    while(k--){
        int s; cin >> s;
    }

}